						<h2 id="content">Iubim animalele</h2>
						<p>Din acest motiv, portile noastre sunt deschise tot timpul vizitatorilor. Adoptia nu se bazeaza doar pe vizualizarea unei fotografii si alegerea partenerului patruped ideal. Incurajam vizita in vederea alegerii unui patruped INAINTEA adoptiei online. </p>
						<div class="row">
							<div class="6u 12u$(small)">
								<h3>Formularul de adoptie nu contine intrebarea "Ce animal v-ati ales?"!</h3>
								<p>Datorita faptului ca va incurajam sa va alegeti patrupedul in persoana, in prima faza nu suntem interesati sa stim ce animal va doriti sa adoptati prin intermediul acestui site. In schimb, formularul nostru contine intrebari mult mai relevante pentru asociatiile noastre partenere, precum "In ce zona locuiti?", "Ce fel de mediu social exista in casa dumneavoastra?", "Unde planuiti sa cresteti animalul respectiv?", "Va avea patrupedul acces si in interior dar si in exterior?". Astfel de intrebari ne fac sa intelegem mai bine tipul dumneavoastra de adoptator.</p>
							</div>
							<div class="6u$ 12u$(small)">
								<h3>"Cat costa?"</h3>
								<p>Adoptia este o procedura GRATUITA care se realizeaza in baza unui contract de adoptie (in cazul de fata, online). Adoptiile nostre raman sub supraveghere foarte atenta chiar si dupa ce patrupedul a parasit asociatia de provenienta. Realizam vizite la domiciliu, apeluri spontane, si ne dorim tot timpul sa stim starea blanosilor care ne-au trecut pragul. </p>
							</div>
							<!-- Break -->
							<div class="4u 12u$(medium)">
								<h3>"Nu s-a adaptat. Il pot aduce inapoi la adapost?"</h3>
								<p>Ne este greu sa credem ca patrupezii disponibili pentru adoptie nu se vor adapta unui mediu primitor si iubitor. Astfel, avem nevoie de dovezi concrete in privinta adoptiei ca nefiind valida pentru dumneavoastra sau pentru blanosul nostru. In acest caz, puteti "returna" patrupedul, dar doar in conditii extraordinar de speciale.</p>
							</div>
							<div class="4u 12u$(medium)">
								<h3>"Nu imi permit sa il sterilizez."</h3>
								<p>Fiecare patruped care pleaca dintr-o asociatie partenera, vine la pachet cu un contract de adoptie care stipuleaza clar ca animalutul va fi sterilizat GRATUIT atunci cand implineste varsta necesara desfasurarii acestei proceduri. De cele mai multe ori, daca nu sunt pui, animalutele noastre sunt deja sterilizate, deparazitate si cu scheme complete de vaccinare, inclusiv microcip.</p>
